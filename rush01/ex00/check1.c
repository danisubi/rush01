#include <unistd.h>

int ft_check1(int *parr)
{
	int i;

	i = 0;
	while(parr[i] != '\0')
	{
		if(i<4 || (i >= 8 && i < 12))
		{
			if((parr[i] + parr[i+4] > 5) || (parr[i] + parr[i+4] < 3)) 
			{
				write(1, "Error\n", 6);
				return(1);
			}		
		}
		i++;
	}
	return (0);
}
